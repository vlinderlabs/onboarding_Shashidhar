let grade: string = "A";

switch(grade){
    case 'A':
        console.log("Excellent...");
        break;
    case 'B':
        console.log("Very Good...");
        break;
    case 'C':
        console.log("Good...");
        break;
    case 'D':
        console.log("Better luck next time...");
        break;
    default:
        console.log("Invalid grade...");

}
